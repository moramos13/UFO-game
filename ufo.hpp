#include <iostream>
#include <vector>

void display(int misses);
std::string set_codeword();
std::string set_answer();

// Declare variables
std::string codeword;
std::string answer;
int misses;
std::vector<char> incorrect;
bool guess;

void inicialize_variables(){
  codeword = set_codeword();
  answer = set_answer();
  misses = 0;
  incorrect = {};
  guess = false;
}

//Define set_codeword
std::string set_codeword()
{
  srand (time(NULL));
  int c = rand() % 5;
  
  std::vector<std::string> codewords = {"codecademy", "guitarra", "amarillo", "estrepitoso", "aladdin", "mario"};
  
  return codewords[c];
}

//Define set_answer
std::string set_answer (){
  std::string answ;
  for (int i = 0; i < codeword.size(); i++){
    answ.push_back('_');
  }
  return answ;
}

// Define take_turn()
void take_turn (){
  display(misses);
  
  std::cout << "\nIncorrect Guesses: \n";
  for (int i = 0; i < incorrect.size(); i++){
    std::cout << incorrect[i] << " ";
  }
  
  std::cout << "\nCodeword: \n";
  for (int i = 0; i < answer.size(); i++){
    std::cout << answer[i] << " ";
  }
  
  char letter;
  std::cout << "\n\nPlease enter your guess: ";
  std::cin >> letter;
  
  for (int i = 0; i < codeword.size(); i++){
    if(codeword[i] == letter){
      guess = true;
      answer[i] = letter;
    } 
  }
  
  if(guess){
    std::cout << "Correct!\n";
  }
  else{
      std::cout << "Incorrect! The tractor beam pulls the person in further.\n";
      incorrect.push_back(letter);
      misses++;
    }
  
  guess = false;
  
}



